package lesson4;

public class VariousTest {

    public static void main(String[] args) {
        int a = 0;
        System.out.println("a = " + a);
        int b = a--;
        int c = 0;
        int d = --c;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
    }
}
